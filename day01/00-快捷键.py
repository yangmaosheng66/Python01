# 编辑类：
# Ctrl + C 直接在要复制的哪一行按下快捷键，即可将当前行内容复制到剪切板
# Ctrl + D 复制当前行到下一行
# Ctrl + Y 删除选定的行
# Ctrl + Alt + L 代码格式化
# Ctrl + Alt + O 优化导入（去掉用不到的包导入）
# Ctrl + 鼠标 简介/进入代码定义
# Ctrl + / 行注释 、取消注释
# Ctrl + 左方括号 快速跳到当前行代码开头
# Ctrl + 右方括号 快速跳到当前行代码末尾
# Shift + F10 运行
# Shift + F9 调试

# 查找/替换类：
# Ctrl + F 当前文件查找
# Ctrl + R 当前文件替换
# Ctrl + Shift + F 全局查找
# Ctrl + Shift + R 全局替换

# 运行类：
# Shift + F10 运行
# Shift + F9 调试
# Alt + Shift + F10 运行模式配置
# Alt + Shift + F9 调试模式配置

# 调试类：
# F8 单步调试（一行一行走）
# F7 进入内部
# Shift + F8 退出
# Ctrl + F8 在当前行加上断点/断点开关
# Ctrl + Shift + F8 查看所有断点


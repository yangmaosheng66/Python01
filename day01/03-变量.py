# 当Python代码只有一行空行时，ctrl+/ 单行注释无法使用，需要在下面插入一个空行
# ctrl+enter 在下方插入一个空行，光标仍在本行
# shift enter  在下方新建一个空行，光标移至下一行
# 变量：内存地址的别名，作用：用来存储数据的
# 在程序中想要存储数据，就需要使用变量
# 变量的定义：变量名 = 数据值   变量名要遵循标识符的定义规则
# 定义一个变量 name，变量 name 中存储的数据是 'isaac'
name = 'isaac'
# 定义一个变量 age，变量中存储的数据值是 18
age = 18

# 使用变量中的值，直接使用变量即可
# 使用print函数打印输出 name 变量中存储的数据值
print(name)
print(age)
# 修改变量中的数据值 变量名 = 新的数据值
# 将 19 这个数据存储到变量 age 中
age = 19
print(age)
